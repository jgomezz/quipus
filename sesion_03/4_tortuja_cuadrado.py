import turtle

if __name__ == "__main__":

    # Velocidad del dibujo
    turtle.speed(1)

    # Posicion (0,0)
    turtle.home() 

    # Dibuja una linea de 100 pixeles
    turtle.forward(100)

    # Rota 90 grados a la derecha
    turtle.left(90)
    turtle.forward(100)

    turtle.left(90)
    turtle.forward(100)

    turtle.left(90)
    turtle.forward(100)

    turtle.done()
